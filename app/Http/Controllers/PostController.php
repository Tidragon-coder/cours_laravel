     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        {
            // Récupère tous les posts avec leurs utilisateurs associés
            $posts = Post::with('user')->orderBy('created_at', 'desc')->get();
            
            // Si c'est une requête AJAX, retourne du JSON
            if (request()->ajax()) {
                return response()->json($posts);
            }
            
            // Sinon, affiche la vue avec les posts
            return view('auth.index', compact('posts'));
        }
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $request->validate([
            'content' => 'required|string|max:1000',
            'image' => 'nullable|image|mimes:jpeg,png,jpg,gif|max:2048',
        ]);

        $imagePath = null;
        if ($request->hasFile('image')) {
            $imagePath = $request->file('image')->store('posts', 'public');
        }

        $post = Post::create([
            'user_id' => auth()->id(),
            'content' => $request->content,
            'image' => $imagePath,
        ]);

        return redirect()->back()->with('success', 'Post créé avec succès !');
    }


